"""### Categorizing Track Durations into Buckets

Now, let's categorize the tracks into 'short' (<180s), 'medium' (180-240s), and 'long' (>240s) durations to further analyze their distribution and characteristics.
"""

def categorize_duration(seconds):
    if seconds < 180:
        return 'Short'
    elif 180 <= seconds <= 240:
        return 'Medium'
    else:
        return 'Long'

data['duration_bucket'] = (data['duration_minutes'] * 60 + data['duration_seconds']).apply(categorize_duration)

display(data[['song', 'duration_minutes', 'duration_seconds', 'duration_bucket']].head())

