import networkx as nx
import matplotlib.pyplot as plt

"""### Collaboration Network Graph

To visualize the collaboration network, we'll create a graph where nodes are artists and an edge exists between two artists if they have collaborated on a song.
"""

# Filter for collaborative songs
collaborative_songs_df = data[data['collaboration_type'] == 'Collaboration']

# Create a graph
G = nx.Graph()

# Add nodes and edges based on collaborations
for idx, row in collaborative_songs_df.iterrows():
    song = row['song']
    artists_in_song = collaborative_songs_df[(collaborative_songs_df['date'] == row['date']) & (collaborative_songs_df['song'] == song)]['artist'].tolist()

    # Add nodes (artists)
    for artist in artists_in_song:
        G.add_node(artist)

    # Add edges between collaborating artists in the same song
    for i in range(len(artists_in_song)):
        for j in range(i + 1, len(artists_in_song)):
            G.add_edge(artists_in_song[i], artists_in_song[j])

print(f"Number of nodes (artists involved in collaborations): {G.number_of_nodes()}")
print(f"Number of edges (collaborations): {G.number_of_edges()}")

# Visualize the collaboration network
plt.figure(figsize=(15, 12))
pos = nx.spring_layout(G, k=0.15, iterations=20)  # position nodes using Fruchterman-Reingold force-directed algorithm
nx.draw_networkx_nodes(G, pos, node_size=200, node_color='skyblue')
nx.draw_networkx_edges(G, pos, width=1, alpha=0.5, edge_color='gray')
nx.draw_networkx_labels(G, pos, font_size=8, font_weight='bold')
plt.title('Artist Collaboration Network', size=20)
plt.axis('off')
plt.show()

